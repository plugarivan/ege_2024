'''
Текстовый файл 24-181.txt содержит строку из заглавных латинских букв и точек, всего не более 10**6 символов. Определите максимальное количество идущих подряд символов, среди которых не более пяти точек.
'''
s = '.' + open('files/24-181.txt').readline() + '.'
a = [i for i in range(len(s)) if s[i] == '.']
print(max(a[i + 6] - a[i] - 1 for i in range(len(a) - 6)))
